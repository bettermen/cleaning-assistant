def get_categories() -> list:
    """获取所有知识分类"""
    return list(KNOWLEDGE_BASE.keys())


def search_knowledge(query: str) -> list:
    """搜索知识库，返回匹配的知识条目"""
    results = []
    query_lower = query.lower()

    for category, topics in KNOWLEDGE_BASE.items():
        for topic, content in topics.items():
            score = 0
            # 分类名匹配
            if query_lower in category.lower():
                score += 3
            # 主题名匹配
            if query_lower in topic.lower():
                score += 5
            # 内容匹配
            content_text = json.dumps(content, ensure_ascii=False)
            if query_lower in content_text.lower():
                score += 1

            if score > 0:
                results.append({
                    "category": category,
                    "topic": topic,
                    "content": content,
                    "score": score,
                })

    results.sort(key=lambda x: -x["score"])
    return results


def format_knowledge(result: dict) -> str:
    """格式化输出一条知识"""
    topic = result["topic"]
    category = result["category"]
    content = result["content"]
    lines = [f"\n🧹 【{category}·{topic}】\n"]

    for key, value in content.items():
        if key in ("方法", "配方1", "配方2", "配方3", "配方4"):
            lines.append(f"📝 {key}：\n  {value.replace(chr(10), chr(10)+'  ')}")
        elif key in ("原则",):
            lines.append(f"📋 {key}：\n  {value.replace(chr(10), chr(10)+'  ')}")
        else:
            lines.append(f"📌 {key}：{value}")

    return "\n".join(lines)


